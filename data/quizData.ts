export interface QuizQuestion {
  id: string;
  statement: string;
  isTrue: boolean;
  explanation: string;
}

export interface RelatedExamQuestion {
  id: string;
  sourceTitle: string;
  statement: string;
  isTrue: boolean;
  explanation: string;
  relatedPassage?: string; // 이 줄을 추가하세요!
}

export interface CharacterRelation {
  from: string;
  to: string;
  type: "supporter" | "antagonist" | "family" | "neutral";
  label: string;
}

export interface CharacterMapData {
  characters: { name: string; role: string; description: string }[];
  relations: CharacterRelation[];
}

export type NarrativePhase =
  | "exposition"
  | "rising"
  | "climax"
  | "falling"
  | "resolution";

export interface NarrativeSection {
  phase: NarrativePhase;
  title: string;
  summary: string;
}

export type GrandUnit = "literature" | "non_fiction";

export interface QuizPassage {
  id: string;
  grandUnit: GrandUnit;
  categoryId: string;
  title: string;
  author: string;
  source: string;
  passage: string;
  originalText?: string;
  modernText?: string;
  questions: QuizQuestion[];
  relatedExams?: RelatedExamQuestion[];
  narrativePhase?: NarrativePhase;
  narrativeSections?: NarrativeSection[];
  characterMap?: CharacterMapData;
}

